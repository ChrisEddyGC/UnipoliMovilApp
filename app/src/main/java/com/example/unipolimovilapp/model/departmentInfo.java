package com.example.unipolimovilapp.model;

public class departmentInfo {
    //las variables en esta seccion deben tener el mismo nombre que el campo dentro de la coleccion UniversityStaff
    String email, jobTitle, phone, userName, workingHours;

    public departmentInfo(){

    }

    public String getEmail() {
        return email;
    }

    public String getJobTitle() {
        return jobTitle;
    }

    public String getPhone() {
        return phone;
    }

    public String getUserName() {
        return userName;
    }

    public String getWorkingHours() {
        return workingHours;
    }
}
